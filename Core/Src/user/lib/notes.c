int16_t sine_wave[2 * SINE_WAVE_SAMPLES + 3];

void init_sine_wave() {
  for (uint32_t i = 2; i < SINE_WAVE_SAMPLES * 2 + 2; i += 2) {
    sine_wave[i] =
        (int16_t)(100 * sin(2 * M_PI * i / SINE_WAVE_SAMPLES) + 200);
		sine_wave[i + 1] = 2;
  }
	sine_wave[0] = 2000;
	sine_wave[1] = 2000;
	sine_wave[SINE_WAVE_SAMPLES * 2 + 2] = END_OF_MELODY;
}

